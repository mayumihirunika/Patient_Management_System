    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String AccountantId=txtAccountantId.getText();
        String AccountantName=txtAccountantName.getText();
        String AccountantAge=txtAccountantAge.getText();
        String AccountantPhone=txtAccountantPhone.getText();
        String AccountantType = cmbAccountantType.getSelectedItem().toString();
        String AccountantGender = cmbAccountantGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();
        
        
        //Check Values
        if (AccountantId.isEmpty() || AccountantName.isEmpty() || AccountantAge.isEmpty() || AccountantPhone.isEmpty() || AccountantType.isEmpty() || AccountantGender.isEmpty() || YOE.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in the fields with valid data", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

        try {
            String sql = "INSERT INTO accountant(Accountant_Id,Accountant_Name,Accountant_Age,Accountant_Phone,Accountant_Type,Accountant_Gender,Y_O_E)values(?,?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1,AccountantId);
            pst.setString(2,AccountantName);
            pst.setString(3,AccountantAge);
            pst.setString(4,AccountantPhone);
            pst.setString(5,AccountantType);
            pst.setString(6,AccountantGender);
            pst.setString(7,YOE);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Accountant Inserted");
            Accountant_table();

        } catch (SQLException ex) {
            Logger.getLogger(Accountant_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
    }//GEN-LAST:event_btnAddActionPerformed

