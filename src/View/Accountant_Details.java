    public void Accountant_table(){
        try {
            pst=con.prepareStatement("Select*from accountant");
            rs=pst.executeQuery();
        
            ResultSetMetaData RSD= rs.getMetaData();
            int R;
            
            R=RSD.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)tblAccountant.getModel();
            df.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v1=new Vector();
                
                for(int i=1; i<=R; i++){
                
                
                v1.add(rs.getString("Accountant_Id"));
                v1.add(rs.getString("Accountant_Name"));
                v1.add(rs.getString("Accountant_Age"));
                v1.add(rs.getString("Accountant_Phone"));
                v1.add(rs.getString("Accountant_Type"));
                v1.add(rs.getString("Accountant_Gender"));
                v1.add(rs.getString("Y_O_E"));
                
                }
                
                df.addRow(v1);
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(Doctor_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    
    

