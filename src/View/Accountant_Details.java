    private void btnRetviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetviewActionPerformed
        // TODO add your handling code here:
        String id = txtAccountantId.getText();
        try{
            Connection connection =  con= DB_Connection.ConnectDB();

            String query = "SELECT Accountant_Name,Accountant_Age,Accountant_Phone,Accountant_Type,Accountant_Gender,Y_O_E FROM accountant WHERE Accountant_Id = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1,id);

            ResultSet resultSet = preparedStatement.executeQuery();

            if(resultSet.next()==true){
                String AccountantName = resultSet.getString(1);
                String AccountantAge = resultSet.getString(2);
                String AccountantPhone = resultSet.getString(3);
                String AccountantType = resultSet.getString(4);
                String AccountantGender= resultSet.getString(5);
                String YOE= resultSet.getString(6);

                txtAccountantName.setText(AccountantName);
                txtAccountantAge.setText(AccountantAge);
                txtAccountantPhone.setText(AccountantPhone);
                cmbAccountantType.setSelectedItem(AccountantType);
                cmbAccountantGender.setSelectedItem(AccountantGender);
                txtYOE.setText(YOE);

            }else{
                txtAccountantId.setText("");
                txtAccountantName.setText("");
                txtAccountantAge.setText("");
                txtAccountantPhone.setText("");
                txtYOE.setText("");

                JOptionPane.showMessageDialog(null, "Invalid Accountant_Id", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }catch(SQLException ex){
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnRetviewActionPerformed

