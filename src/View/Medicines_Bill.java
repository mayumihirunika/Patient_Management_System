    public void medicine_table(){
        try {
            pst=con.prepareStatement("Select*from medicine");
            rs=pst.executeQuery();
        
            ResultSetMetaData RSD= rs.getMetaData();
            int R;
            
            R=RSD.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)tblMedicines.getModel();
            df.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v1=new Vector();
                
                for(int i=1; i<=R; i++){
                
                
                v1.add(rs.getString("Medicines_Id"));
                v1.add(rs.getString("Medicines_Name"));
                v1.add(rs.getString("Medicines_Price"));
                v1.add(rs.getString("Company_Name"));
                v1.add(rs.getString("Qty"));
                              
                }
                
                df.addRow(v1);
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(Medicines_Bill.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    

     private boolean isItemInvoice(int mediId) {
        for (int i = 0; i < tblMBill.getRowCount(); i++) {
            int exstingItemID = (int) tblMBill.getValueAt(i, 0);
            if (mediId == exstingItemID) {
                return true;
            }
        }
        return false;
    }
