    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if(x == JFileChooser.APPROVE_OPTION)
        {
            path = j.getSelectedFile().getPath();
        }
        com.lowagie.text.Document doc = new com.lowagie.text.Document();
        try{
            PdfWriter.getInstance(doc, new FileOutputStream(path +"MediBill.pdf"));
            doc.open();
            PdfPTable tbl = new PdfPTable(5);
            tbl.addCell("Medicines_ID");
            tbl.addCell("Medicines_Name");
            tbl.addCell("Medicines_Price");
            tbl.addCell("Qty");
            tbl.addCell("Company_Name");
           
            for(int i=0; i< tblMBill.getRowCount(); i++){
                String FN = tblMBill.getValueAt(i, 0).toString();
                String LN = tblMBill.getValueAt(i, 1).toString();
                String DN = tblMBill.getValueAt(i, 2).toString();
                String CN = tblMBill.getValueAt(i, 3).toString();
                String HN = tblMBill.getValueAt(i, 4).toString();
               
                
                tbl.addCell(FN);
                tbl.addCell(DN);
                tbl.addCell(CN);
                tbl.addCell(HN);
            }
            doc.add(tbl);
            JOptionPane.showMessageDialog(null, "PDF Generated");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Some thing went wrong");
        }
        doc.close();
    }//GEN-LAST:event_btnPrintActionPerformed

