    public void nurse_table(){
        try {
            pst=con.prepareStatement("Select*from nurse");
            rs=pst.executeQuery();
        
            ResultSetMetaData RSD= rs.getMetaData();
            int R;
            
            R=RSD.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)tblNurse.getModel();
            df.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v1=new Vector();
                
                for(int i=1; i<=R; i++){
                
                
                v1.add(rs.getString("Nurse_Id"));
                v1.add(rs.getString("Nurse_Name"));
                v1.add(rs.getString("Nurse_Age"));
                v1.add(rs.getString("Nurse_Phone"));
                v1.add(rs.getString("Nurse_Type"));
                v1.add(rs.getString("Nurse_Gender"));
                v1.add(rs.getString("Y_O_E"));
                
                }
                
                df.addRow(v1);
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(Nurse_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
