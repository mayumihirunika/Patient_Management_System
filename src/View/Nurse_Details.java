    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String NurseId=txtNurseId.getText();
        String NurseName=txtNurseName.getText();
        String NurseAge=txtNurseAge.getText();
        String NursePhone=txtNursePhone.getText();
        String NurseType = cmbNurseType.getSelectedItem().toString();
        String NurseGender = cmbNurseGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();
        
        
        //Check Values
        if (NurseId.isEmpty() || NurseName.isEmpty() || NurseAge.isEmpty() || NursePhone.isEmpty() || NurseType.isEmpty() || NurseGender.isEmpty() || YOE.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in the fields with valid data", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

        try {
            String sql = "INSERT INTO nurse(Nurse_Id,Nurse_Name,Nurse_Age,Nurse_Phone,Nurse_Type,Nurse_Gender,Y_O_E)values(?,?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1,NurseId);
            pst.setString(2,NurseName);
            pst.setString(3,NurseAge);
            pst.setString(4,NursePhone);
            pst.setString(5,NurseType);
            pst.setString(6,NurseGender);
            pst.setString(7,YOE);
          
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Nurse Inserted");
            nurse_table();
       

        } catch (SQLException ex) {
            Logger.getLogger(Nurse_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String NurseId=txtNurseId.getText();
        String NurseName=txtNurseName.getText();
        String NurseAge=txtNurseAge.getText();
        String NursePhone=txtNursePhone.getText();
        String NurseType = cmbNurseType.getSelectedItem().toString();
        String NurseGender = cmbNurseGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();



        try {
            pst=con.prepareStatement("Update nurse set Nurse_Name=?,Nurse_Age=?,Nurse_Phone=?,Nurse_Type=?,Nurse_Gender=?,Y_O_E=? where Nurse_Id=?");
            
            pst.setString(1,NurseName);
            pst.setString(2,NurseAge);
            pst.setString(3,NursePhone);
            pst.setString(4,NurseType);
            pst.setString(5,NurseGender);
            pst.setString(6,YOE);
            pst.setString(7,NurseId);
           
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Nurse Updated");
            nurse_table();
            
            btnAdd.setEnabled(true);
            

        } catch (SQLException ex) {
            Logger.getLogger(Nurse_Details.class.getName()).log(Level.SEVERE, null, ex);
        }

                                             
    }//GEN-LAST:event_btnUpdateActionPerformed

