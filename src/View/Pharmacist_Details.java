     public void Pharmacist_table(){
        try {
            pst=con.prepareStatement("Select*from Pharmacist");
            rs=pst.executeQuery();
        
            ResultSetMetaData RSD= rs.getMetaData();
            int R;
            
            R=RSD.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)tblPharmacist.getModel();
            df.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v1=new Vector();
                
                for(int i=1; i<=R; i++){
                
                
                v1.add(rs.getString("Pharmacist_Id"));
                v1.add(rs.getString("Pharmacist_Name"));
                v1.add(rs.getString("Pharmacist_Age"));
                v1.add(rs.getString("Pharmacist_Phone"));
                v1.add(rs.getString("Pharmacist_Type"));
                v1.add(rs.getString("Pharmacist_Gender"));
                v1.add(rs.getString("Y_O_E"));
                
                }
                
                df.addRow(v1);
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(Pharmacist_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
