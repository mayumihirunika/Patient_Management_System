    private void tblPharmacistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPharmacistMouseClicked
        // TODO add your handling code here:
        DefaultTableModel d1=(DefaultTableModel)tblPharmacist.getModel();
        int SelectIndex=tblPharmacist.getSelectedRow();

        txtPharmacistId.setText(d1.getValueAt(SelectIndex,0).toString());
        txtPharmacistName.setText(d1.getValueAt(SelectIndex,1).toString());
        txtPharmacistAge.setText(d1.getValueAt(SelectIndex,2).toString());
        txtPharmacistPhone.setText(d1.getValueAt(SelectIndex,3).toString());
        cmbPharmacistType.setSelectedItem(d1.getValueAt(SelectIndex,4).toString());
        cmbPharmacistGender.setSelectedItem(d1.getValueAt(SelectIndex,5).toString());
        txtYOE.setText(d1.getValueAt(SelectIndex,6).toString());

        btnAdd.setEnabled(false);
    }//GEN-LAST:event_tblPharmacistMouseClicked

