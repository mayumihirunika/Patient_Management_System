    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String PharmacistId=txtPharmacistId.getText();
        String PharmacistName=txtPharmacistName.getText();
        String PharmacistAge=txtPharmacistAge.getText();
        String PharmacistPhone=txtPharmacistPhone.getText();
        String PharmacistType = cmbPharmacistType.getSelectedItem().toString();
        String PharmacistGender = cmbPharmacistGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();
        
        
        //Check Values
        if (PharmacistId.isEmpty() || PharmacistName.isEmpty() || PharmacistAge.isEmpty() || PharmacistPhone.isEmpty() || PharmacistType.isEmpty() || PharmacistGender.isEmpty() || YOE.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in the fields with valid data", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

        try {
            String sql = "INSERT INTO pharmacist(Pharmacist_Id,Pharmacist_Name,Pharmacist_Age,Pharmacist_Phone,Pharmacist_Type,Pharmacist_Gender,Y_O_E)values(?,?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1,PharmacistId);
            pst.setString(2,PharmacistName);
            pst.setString(3,PharmacistAge);
            pst.setString(4,PharmacistPhone);
            pst.setString(5,PharmacistType);
            pst.setString(6,PharmacistGender);
            pst.setString(7,YOE);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Pharmacist Inserted");
            Pharmacist_table();

        } catch (SQLException ex) {
            Logger.getLogger(Pharmacist_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String PharmacistId=txtPharmacistId.getText();
        String PharmacistName=txtPharmacistName.getText();
        String PharmacistAge=txtPharmacistAge.getText();
        String PharmacistPhone=txtPharmacistPhone.getText();
        String PharmacistType = cmbPharmacistType.getSelectedItem().toString();
        String PharmacistGender = cmbPharmacistGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();

        try {
            pst=con.prepareStatement("Update Pharmacist set Pharmacist_Name=?,Pharmacist_Age=?,Pharmacist_Phone=?,Pharmacist_Type=?,Pharmacist_Gender=?,Y_O_E=? where Pharmacist_Id=?");

            pst.setString(1,PharmacistName);
            pst.setString(2,PharmacistAge);
            pst.setString(3,PharmacistPhone);
            pst.setString(4,PharmacistType);
            pst.setString(5,PharmacistGender);
            pst.setString(6,YOE);
            pst.setString(7,PharmacistId);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Pharmacist Updated");
            Pharmacist_table();

            btnAdd.setEnabled(true);

        } catch (SQLException ex) {
            Logger.getLogger(Pharmacist_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

