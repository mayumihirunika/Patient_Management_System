    public void doctor_table(){
        try {
            pst=con.prepareStatement("Select*from doctor");
            rs=pst.executeQuery();
        
            ResultSetMetaData RSD= rs.getMetaData();
            int R;
            
            R=RSD.getColumnCount();
            
            DefaultTableModel df=(DefaultTableModel)tblDoctor.getModel();
            df.setRowCount(0);
            
            
            while(rs.next())
            {
                Vector v1=new Vector();
                
                for(int i=1; i<=R; i++){
                
                
                v1.add(rs.getString("Doctor_Id"));
                v1.add(rs.getString("Doctor_Name"));
                v1.add(rs.getString("Doctor_Age"));
                v1.add(rs.getString("Doctor_Phone"));
                v1.add(rs.getString("Doctor_Type"));
                v1.add(rs.getString("Doctor_Gender"));
                v1.add(rs.getString("Y_O_E"));
                
                }
                
                df.addRow(v1);
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(Doctor_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    

