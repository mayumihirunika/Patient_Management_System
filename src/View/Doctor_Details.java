    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String DoctorId=txtDoctorId.getText();
        String DoctorName=txtDoctorName.getText();
        String DoctorAge=txtDoctorAge.getText();
        String DoctorPhone=txtDoctorPhone.getText();
        String DoctorType = cmbDoctorType.getSelectedItem().toString();
        String DoctorGender = cmbDoctorGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();
        
        //Check Values
        if (DoctorId.isEmpty() || DoctorName.isEmpty() || DoctorAge.isEmpty() || DoctorPhone.isEmpty() || DoctorType.isEmpty() || DoctorGender.isEmpty() ||YOE.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in the fields with valid data", "Error", JOptionPane.ERROR_MESSAGE);
        }else{

        try {
            String sql = "INSERT INTO doctor(Doctor_Id,Doctor_Name,Doctor_Age,Doctor_Phone,Doctor_Type,Doctor_Gender,Y_O_E)values(?,?,?,?,?,?,?)";
            pst=con.prepareStatement(sql);
            pst.setString(1,DoctorId);
            pst.setString(2,DoctorName);
            pst.setString(3,DoctorAge);
            pst.setString(4,DoctorPhone);
            pst.setString(5,DoctorType);
            pst.setString(6,DoctorGender);
            pst.setString(7,YOE);
          
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"Doctor Inserted");
            doctor_table();
       

        } catch (SQLException ex) {
            Logger.getLogger(Doctor_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String DoctorId=txtDoctorId.getText();
        String DoctorName=txtDoctorName.getText();
        String DoctorAge=txtDoctorAge.getText();
        String DoctorPhone=txtDoctorPhone.getText();
        String DoctorType = cmbDoctorType.getSelectedItem().toString();
        String DoctorGender = cmbDoctorGender.getSelectedItem().toString();
        String YOE=txtYOE.getText();


        try {
            pst=con.prepareStatement("Update doctor set Doctor_Name=?,Doctor_Age=?,Doctor_Phone=?,Doctor_Type=?,Doctor_Gender=?,Y_O_E=? where Doctor_Id=?");
            
            pst.setString(1,DoctorName);
            pst.setString(2,DoctorAge);
            pst.setString(3,DoctorPhone);
            pst.setString(4,DoctorType);
            pst.setString(5,DoctorGender);
            pst.setString(6,YOE);
            pst.setString(7,DoctorId);
          
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"doctor Updated");
            doctor_table();
            
            btnAdd.setEnabled(true);
            

        } catch (SQLException ex) {
            Logger.getLogger(Doctor_Details.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

